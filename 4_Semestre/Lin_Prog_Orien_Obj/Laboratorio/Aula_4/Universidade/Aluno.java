package Aula_4.Universidade;

public class Aluno {
    private String nome;
    private int idade;

    public void setNome(String n) {
        nome = n;
    }

    public void setIdade(int n) {
        idade = n;
    }

    public String getNome() {
        return nome;
    }

    public int getIdade() {
        return idade;
    }
}
